/**
  * @brief  Configures the CAN, transmit and receive by polling
  * @param  None
  * @retval PASSED if the reception is well done, FAILED in other case
  */
HAL_StatusTypeDef CAN_Polling(void)
{
  CAN_FilterConfTypeDef  sFilterConfig;
  static CanTxMsgTypeDef        TxMessage;
  static CanRxMsgTypeDef        RxMessage;
  
  /*##-1- Configure the CAN peripheral #######################################*/
  hcan.pTxMsg = &TxMessage;
  hcan.pRxMsg = &RxMessage;

  
  /*##-2- Configure the CAN1 Filter ###########################################*/
  sFilterConfig.FilterNumber = 0;
  sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
  sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
  sFilterConfig.FilterIdHigh = 0x0000;
  sFilterConfig.FilterIdLow = 0x0000;
  sFilterConfig.FilterMaskIdHigh = 0x0000;
  sFilterConfig.FilterMaskIdLow = 0x0000;
  sFilterConfig.FilterFIFOAssignment = 0;
  sFilterConfig.FilterActivation = ENABLE;
  sFilterConfig.BankNumber = 14;
  
  if (HAL_CAN_ConfigFilter(&hcan, &sFilterConfig) != HAL_OK)
  {
    /* Filter configuration Error */
    Error_Handler();
  }
  
  /*##-3- Configure Transmission process #####################################*/
  hcan.pTxMsg->StdId = 0x11;
  hcan.pTxMsg->ExtId = 0x01;
  hcan.pTxMsg->RTR = CAN_RTR_DATA;
  hcan.pTxMsg->IDE = CAN_ID_STD;
  hcan.pTxMsg->DLC = 2;
  hcan.pTxMsg->Data[0]=0xCA; 
  hcan.pTxMsg->Data[1]=0xFE; 
  
  if(HAL_CAN_Transmit(&hcan, 10) != HAL_OK)
  {
    /* Transmition Error */
    Error_Handler();
  }
  if(HAL_CAN_GetState(&hcan) != HAL_CAN_STATE_READY)
  {
     Error_Handler();
  }
  
  /*##-4- Start the Reception process ########################################*/
  if(HAL_CAN_Receive(&hcan, CAN_FIFO0,10) != HAL_OK)
  {
    /* Reception Error */
    Error_Handler();
  }
  
  if(HAL_CAN_GetState(&hcan) != HAL_CAN_STATE_READY)
  {
    return HAL_ERROR;
  }
  
  if(hcan.pRxMsg->StdId != 0x11)
  {
    return HAL_ERROR;  
  }

  if(hcan.pRxMsg->IDE != CAN_ID_STD)
  {
    return HAL_ERROR;
  }

  if(hcan.pRxMsg->DLC != 2)
  {
    return HAL_ERROR;  
  }

  if((hcan.pRxMsg->Data[0]<<8|RxMessage.Data[1]) != 0xCAFE)
  {
    return HAL_ERROR;
  }
  
  return HAL_OK; /* Test Passed */
}

